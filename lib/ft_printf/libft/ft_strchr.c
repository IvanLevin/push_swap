#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	ch_a;

	ch_a = (char)c;
	while (*s != '\0' && *s != ch_a)
		s++;
	if (*s != ch_a)
		return (NULL);
	else
		return ((char *)s);
}
